export function fightersBornOn(d: Date): FreedomFighter[] {
  const key = `-${String(d.getMonth() + 1).padStart(2, "0")}-${String(d.getDate()).padStart(2, "0")}`;
  return FREEDOM_FIGHTERS.filter((f) => f.birth?.endsWith(key) || f.death?.endsWith(key));
}
